                            <li>Cortamos y limpiamos el lomo de salmón en 4 partes del mismo tamaño y salpimentamos. En una sartén amplia y antiadherente ponemos un chorrito de aceite a fuego fuerte. Cuando esté bien caliente añadimos los lomos de salmón. Dejamos que se cocinen 2 minutos por cada cara y los reservamos en un plato. Con este paso previo sellaremos el salmón, que conservará todos sus jugos cuando lo acabemos de cocinar.</li>
                            <li>En la misma sartén, a fuego medio, añadimos un chorro generoso de aceite. Añadimos el ajo bien picado, junto con una buena cantidad de perejil, también picado muy fino. Dejamos que se cocine 3 minutos, removiendo con frecuencia.</li>
