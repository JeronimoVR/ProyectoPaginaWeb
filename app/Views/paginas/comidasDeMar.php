                            <li>Hecho esto añadimos los guisantes junto con los lomos de salmón que habíamos reservado y los jugos que hayan soltado en el plato. Corregimos de sal y dejamos que se cocinen durante 5 minutos.</li>
                            <li>Pasado ese tiempo añadimos las almejas y dejamos que todo se cocine durante 5 minutos más. Corregimos de sal, servimos en platos y decoramos el salmon en salsa verde con un poco más de perejil fresco.</li>
