                        <h5 class="card-title">Ensalada cesar casera con pollo</h5>
                        <br>
                        <h6>Ingredientes</h6>
                        <ul class="card-text">
                            <li>2 lechugas romanas</li>
                            <li>150ml de aceite de oliva</li>
                            <li>1 huevo</li>
                            <li>200g de queso parmesano</li>
                            <li>jugo de 1/2 limón</li>
                            <li>40g de anchoas en conserva</li>
                            <li>1 cucharadita de mostaza</li>
                            <li>1 diente de ajo</li>
                            <li>100g de picatostes (pan tostado cortado en cubos)</li>
                            <li>1 cucharadita de salsa inglesa «worcester»</li>
                            <li>2 pechugas de pollo</li>
                            <li>tomates cherrys (opcional)</li>
                            <li>rúcula (opcional)</li>
                            <li>Sal y pimienta</li>
                            <li>chorrito de aceite de oliva más, para cocinar las pechugas</li>
                            <br>
                        </ul>
                        <h6>Preparación</h6>
                        <p class="card-text">
                        <ol>
                            <li>Lo más importante de esta receta: La salsa césar. Para prepararla, en un vaso batidor ponemos el huevo, los 200ml de aceite de oliva, y el jugo del limón. Ponemos una batidora manual o turmix en el fondo del mismo y le damos «caña». Pasados 10 segundos, levantamos un poquito la batidora, sin parar de batir. Dejamos 5 segundos más y ya podemos subir y bajar continuamente la batidora, siempre sin parar de batir. Ya tendremos nuestra mayonesa hecha, la base de la salsa césar. Ten en cuenta que debe quedar algo líquida, porque le vamos a añadir a continuación más ingredientes.</li>
                            <li>Para obtener nuestra salsa césar, añadimos a la mayonesa que acabamos de preparar, un diente de ajo, el jugo del limón, las anchoas, escurridas de su aceite, la mostaza, la salsa worcester, y unos 50g del queso parmesano. Volvemos a batir todo. Con estos ingredientes tan sólidos, conseguiremos una salsa con más cuerpo. Si te queda muy densa, puedes añadir un pequeño chorrito de leche y remover, hasta que quede una salsa con cierto cuerpecito pero, tirando a líquida.</li>
                            <li>Una vez preparada la salsa, seguimos con la elaboración de nuestra ensalada César. Salpimentamos y cortamos las pechugas de pollo en cubos de tamaño de bocado. Las ponemos en una sartén, con un chorrito de aceite y a fuego fuerte, y las cocinamos unos minutos. Que cojan color por fuera y se cocinen en su interior.</li>
                            <li>Mientras se cocina el pollo, en el fondo de una ensaladera, ponemos un par de cucharadas de la salsa que acabamos de elaborar. sobre ella, colocamos la lechuga cortada en trozos, los tomates cherrys y la rúcula (estos dos últimos ingredientes, no son típicos de la ensalada cesar. Pero a mí, me encantan) los picatostes, el resto del queso parmesano, cortado en lascas finas y finalmente, el pollo, cuando esté totalmente cocinado. Terminamos aderezando nuestra ensalada con más salsa césar. Sírvelo inmediatamente, porque el calor del pollo, le va a dar un toque de ensalada templada muy interesante.</li>
                            <li>Hecho esto añadimos los guisantes junto con los lomos de salmón que habíamos reservado y los jugos que hayan soltado en el plato. Corregimos de sal y dejamos que se cocinen durante 5 minutos.</li>
                            <li>Pasado ese tiempo añadimos las almejas y dejamos que todo se cocine durante 5 minutos más. Corregimos de sal, servimos en platos y decoramos el salmon en salsa verde con un poco más de perejil fresco.</li>
                        </ol>
